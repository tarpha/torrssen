            </div>
            <div v-if="searchInput != ''" class="remove">
              <button class="clear_button" type="button" @click="remove">
                <span>×</span>
              </button>
            </div>
          </div>
          <button :class="bottonClass" type="button" @click="setSearchInput(searchInput)">
