<script>
import { mapGetters, mapMutations } from 'vuex'
import NuxtAlert from '~/components/Alert'

export default {
  components: {
    NuxtAlert
  },
  data () {
    return {
      searchInput: '',
      bottonClass: 'search-button'
    }
  },
  methods: {
    ...mapMutations(['setSearchInput']),
    remove: function () {
      this.searchInput = ''
      this.setSearchInput('')
    }
  },
  computed: {
    ...mapGetters(['getAlertCount'])
  },
  watch: {
    getAlertCount: {
      handler: function () {
        if (this.getAlertCount === 0) {
          this.bottonClass = 'search-button'
        } else {
          this.bottonClass = 'search-button no-top-right-radius'
        }
      },
      deep: true
    }
  }
}
</script>

